/**
 * @author huzexiong
 */
public class CompanyData {

	private String begin;
	private String end;
	private String token;
	private String companyFilter;
	private String category;
	private String recordCount;
	private String sessionId;
	private String hscode;
	
	/**
	 * @return the begin
	 */
	public String getBegin() {
		return begin;
	}
	/**
	 * @param begin the begin to set
	 */
	public void setBegin(String begin) {
		this.begin = begin;
	}
	/**
	 * @return the end
	 */
	public String getEnd() {
		return end;
	}
	/**
	 * @param end the end to set
	 */
	public void setEnd(String end) {
		this.end = end;
	}
	/**
	 * @return the token
	 */
	public String getToken() {
		return token;
	}
	/**
	 * @param token the token to set
	 */
	public void setToken(String token) {
		this.token = token;
	}
	/**
	 * @return the companyFilter
	 */
	public String getCompanyFilter() {
		return companyFilter;
	}
	/**
	 * @param companyFilter the companyFilter to set
	 */
	public void setCompanyFilter(String companyFilter) {
		this.companyFilter = companyFilter;
	}
	/**
	 * @return the category
	 */
	public String getCategory() {
		return category;
	}
	/**
	 * @param category the category to set
	 */
	public void setCategory(String category) {
		this.category = category;
	}
	/**
	 * @return the recordCount
	 */
	public String getRecordCount() {
		return recordCount;
	}
	/**
	 * @param recordCount the recordCount to set
	 */
	public void setRecordCount(String recordCount) {
		this.recordCount = recordCount;
	}
	/**
	 * @return the sessionId
	 */
	public String getSessionId() {
		return sessionId;
	}
	/**
	 * @param sessionId the sessionId to set
	 */
	public void setSessionId(String sessionId) {
		this.sessionId = sessionId;
	}
	/**
	 * @return the hscode
	 */
	public String getHscode() {
		return hscode;
	}
	/**
	 * @param hscode the hscode to set
	 */
	public void setHscode(String hscode) {
		this.hscode = hscode;
	}
	
}
